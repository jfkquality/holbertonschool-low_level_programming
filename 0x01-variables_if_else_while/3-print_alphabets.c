#include <stdio.h>

/**
 * main - print alphabets
 * Description. Print alphabet in upper & lower case.
 * Return: 0
 */
int main(void)
{
	char c;
	char C;

	c = 'a';
	C = 'A';

	while (c <= 'z')
	{
		putchar(c);
		c++;
	}
	while (C <= 'Z')
	{
		putchar(C);
		C++;
	}
	putchar('\n');

	return (0);
}
